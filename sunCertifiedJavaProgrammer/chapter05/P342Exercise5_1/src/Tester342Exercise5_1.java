class Tester342Exercise5_1 {
    public static void main(String[] args) {
        //  Exercise 5-1
        char x = 'c';
        switch (x) {
            case 'a':
                System.out.println("a");
                break;
            case 'b':
                System.out.println("b");
                break;
            case 'c':
                System.out.println("c");
                break;
            case 'd':
                System.out.println("d");
                break;
            case 'e':
                System.out.println("e");
                break;
            default:
                System.out.println("default");
        }
    }
}
