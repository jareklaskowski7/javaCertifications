class Tester240 {
    public static void main(String[] args) {
        Integer i2 = Integer.valueOf("101011", 2);      //  converts 101011
        //  to 43 and
        //  assigns the value
        //  43 to the
        //  Integer object i2
        System.out.println(i2);

        //  or

        Float f2 = Float.valueOf("3.14f");              //  assigns 3.14 to the
        //  Float object f2
        System.out.println(f2);
    }
}
