class ValueTest {
    public static void main(String[] args) {
        int a = 10;     //  Assign a value to a
        System.out.println("a = " + a);
        int b = a;
        System.out.println("b = " + b);
        b = 30;
        System.out.println("a = " + a + " after change to b");
        System.out.println("b = " + b);
    }
}
