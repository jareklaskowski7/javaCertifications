class Octal {
    public static void main(String[] args) {
        int zero = 0;         //  Equal to decimal 0
        int one = 01;         //  Equal to decimal 1
        int two = 02;         //  Equal to decimal 2
        int three = 03;       //  Equal to decimal 3
        int four = 04;        //  Equal to decimal 4
        int five = 05;        //  Equal to decimal 5
        int six = 06;         //  Equal to decimal 6
        int seven = 07;       //  Equal to decimal 7
        int eight = 010;      //  Equal to decimal 8
        int nine = 011;       //  Equal to decimal 9
        int ten = 012;        //  Equal to decimal 10
        int eleven = 013;     //  Equal to decimal 11
        int twelve = 014;     //  Equal to decimal 12
        int thirteen = 015;   //  Equal to decimal 13
        int fourteen = 016;   //  Equal to decimal 14
        int fifteen = 017;    //  Equal to decimal 15
        int sixteen = 020;    //  Equal to decimal 16
        int seventeen = 021;  //  Equal to decimal 17
        int eighteen = 022;   //  Equal to decimal 18
        int nineteen = 023;   //  Equal to decimal 19
        int twenty = 024;     //  Equal to decimal 20
        int twenty_one = 025; //  Equal to decimal 21
        System.out.println("Octal 0 = " + zero);
        System.out.println("Octal 01 = " + one);
        System.out.println("Octal 02 =  " + two);
        System.out.println("Octal 03 =  " + three);
        System.out.println("Octal 04 = " + four);
        System.out.println("Octal 05 = " + five);
        System.out.println("Octal 06 =  " + six);
        System.out.println("Octal 07 =  " + seven);
        System.out.println("Octal 010 = " + eight);
        System.out.println("Octal 011 = " + nine);
        System.out.println("Octal 012 =  " + ten);
        System.out.println("Octal 013 =  " + eleven);
        System.out.println("Octal 014 =  " + twelve);
        System.out.println("Octal 015 =  " + thirteen);
        System.out.println("Octal 016 = " + fourteen);
        System.out.println("Octal 017 = " + fifteen);
        System.out.println("Octal 020 =  " + sixteen);
        System.out.println("Octal 021 =  " + seventeen);
        System.out.println("Octal 022 = " + eighteen);
        System.out.println("Octal 023 = " + nineteen);
        System.out.println("Octal 024 =  " + twenty);
        System.out.println("Octal 025 =  " + twenty_one);
    }
}
